package Entities;

public class Telefone {
	private String numero;

	// CONSTRUTOR:
	public Telefone(String numero) {
		super();
		this.numero = numero;
	}

	// GETTERS AND SETTERS:
	public String getNumero() {
		return numero;
	}

	public void setNumero(String numero) {
		this.numero = numero;
	}
	
	
}
